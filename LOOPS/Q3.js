// Multiplication Table
let table = 5;

// For Loop
for (let i = 1; i <= 10; i++) {
  console.log(`${table} x ${i} = ${table * i}`);
}

// While Loop
let j1 = 1;
while (j1 <= 10) {
  console.log(`${table} x ${j1} = ${table * j1}`);
  j1++;
}

// Do...While Loop
let j2 = 1;
do {
  console.log(`${table} x ${j2} = ${table * j2}`);
  j2++;
} while (j2 <= 10);